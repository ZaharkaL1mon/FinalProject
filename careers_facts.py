def get_fact_for_career(career_name):
    """
    Возвращает случайный факт о профессии.
    Если профессия не найдена — возвращает общий факт.
    """
    import random

    if career_name in PROFESSION_FACTS:
        return random.choice(PROFESSION_FACTS[career_name])
    else:
        # Общий факт, если профессии нет в списке
        all_facts = []
        for facts in PROFESSION_FACTS.values():
            all_facts.extend(facts)
        return random.choice(all_facts)

